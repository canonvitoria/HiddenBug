//Função Teleporte

const linguagem1 =  document.querySelector('.linguagem-1');
linguagem1.addEventListener('click', ()=>{
    lineX = 4;
    lineY = 6;
    cordX = 13;
    cordY = 9;
    translateX = -10;
    translateY = -3; 
    miniMap.classList.toggle('open-mapa')

   document.documentElement.style.setProperty("--transicao", "2s");
   setTimeout(()=>{
    document.documentElement.style.setProperty("--transicao", ".2s");
  },2000 )
  
});

